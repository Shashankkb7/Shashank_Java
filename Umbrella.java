	{
		System.out.println("press button to open umbrella");
	}
	static void usedAsSunShade()
	{
		System.out.println("umbrella is used as a sun shade");
	}
	static void decoration()
	{
		System.out.println("Used as for decoration purpose");
	}
	static void turnIntoKite()
	{
		System.out.println("umbrella is turn into a kite");
	}
